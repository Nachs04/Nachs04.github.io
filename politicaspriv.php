                Recopilamos información sobre la actividad de la red electrónica y de Internet, como información sobre el uso y el dispositivo. Podemos recopilar información sobre el navegador que está utilizando y podemos ver de qué sitio proviene o qué sitio visita cuando nos deja. También podemos recopilar información sobre su sistema operativo, información del proveedor de servicios de Internet (ISP) y direcciones IP y otros identificadores de dispositivos, como los identificadores de dispositivos móviles. Además, podemos recopilar información sobre su uso de nuestros Servicios, incluso mediante el uso de tecnologías de seguimiento como cookies, etiquetas de píxeles y cookies de Flash.
