                <strong>¿QUÉ INFORMACIÓN RECOPILAMOS?</strong><br>
                Recopilamos identificadores e información de contacto. Por ejemplo, podemos recopilar su nombre y dirección postal si completa nuestros formularios. También podemos recopilar su número de teléfono o dirección de correo electrónico. También recopilamos identificadores en línea. Por ejemplo, si se relaciona con nosotros a través de un sitio de redes sociales, podemos recopilar su identificador de usuario de redes sociales.
